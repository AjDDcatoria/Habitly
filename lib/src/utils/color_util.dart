String getColorString(int index) {
  switch (index) {
    case 0:
      return 'lightCream';
    case 1:
      return 'peach';
    case 2:
      return 'taupe';
    case 3:
      return 'beige';
    case 4:
      return 'coral';
    case 5:
      return 'softPink';
    case 6:
      return 'candyPink';
    case 7:
      return 'lavenderPink';
    case 8:
      return 'softPurple';
    case 9:
      return 'periwinkle';
    case 10:
      return 'babyBlue';
    case 11:
      return 'mutedTeal';
    case 12:
      return 'skyBlue';
    case 13:
      return 'mintGreen';
    case 14:
      return 'creamOrange';
    default:
      return 'lightborder';
  }
}
